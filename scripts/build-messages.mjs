#!/usr/bin/env node
// Generates `src/i18n/messages.gen.ts` from the canonical, hand-authored
// `messages/<locale>.json` catalog.
//
// Why generate? The widgets are embeddable and bundle-budget-constrained
// (≤10 KB gzipped each). A single big catalog object imported by every
// widget would defeat tree-shaking — esbuild can't drop unused
// *properties* of one object, only unused *named exports*. So we emit one
// named export per namespace (ssoConnection, directorySync, error, …),
// each carrying all six locales. A widget that imports `{ ssoConnection,
// error }` then pays only for those two slices; the audit-log strings
// never reach the SSO bundle.
//
// The generated file is committed (so `tsc`/`vitest` work without a
// pregen step) AND regenerated on every `pnpm build` (so npm artifacts
// can never drift from the JSON source of truth).

import fs from "node:fs";
import path from "node:path";

const root = path.resolve(import.meta.dirname, "..");
const messagesDir = path.join(root, "messages");
const outFile = path.join(root, "src", "i18n", "messages.gen.ts");

const LOCALES = ["en", "de", "fr", "es", "ja", "pt-BR"];

const catalogs = {};
for (const loc of LOCALES) {
  const file = path.join(messagesDir, `${loc}.json`);
  catalogs[loc] = JSON.parse(fs.readFileSync(file, "utf8"));
}

// Namespaces = every key under `widgets.*`, plus the top-level `error`.
const widgetNamespaces = Object.keys(catalogs.en.widgets ?? {});

function sliceNamespace(getSubtree) {
  const out = {};
  for (const loc of LOCALES) out[loc] = getSubtree(catalogs[loc]);
  return out;
}

const exportsByName = {};
for (const ns of widgetNamespaces) {
  exportsByName[ns] = sliceNamespace((c) => c.widgets[ns]);
}
exportsByName.error = sliceNamespace((c) => c.error);

const header = `// AUTO-GENERATED by scripts/build-messages.mjs — DO NOT EDIT.
// Source of truth: messages/<locale>.json. Run \`pnpm build\` (or
// \`node scripts/build-messages.mjs\`) to regenerate after editing the
// catalog. One named export per namespace keeps the per-widget bundles
// tree-shakeable (see the script header for the rationale).
/* eslint-disable */
import type { LocaleCatalog } from "./index";
`;

const body = Object.entries(exportsByName)
  .map(
    ([name, value]) =>
      `\nexport const ${name}: LocaleCatalog = ${JSON.stringify(value, null, 2)} as const;\n`,
  )
  .join("");

fs.writeFileSync(outFile, header + body, "utf8");
console.log(
  `[i18n] wrote ${path.relative(root, outFile)} — ${Object.keys(exportsByName).length} namespaces × ${LOCALES.length} locales`,
);
